class Solution:
    def wordBreak(self, s, wordDict):
        """
        :type s: str
        :type wordDict: List[str]
        :rtype: bool
        """

        if s == "":
        		return True

        X = [False] * (len(s))
        for i in range(0, len(s)):
        		if (i == 0) or (X[i - 1] == True):
        				for word in wordDict:
        						if s[i:i + len(word)] == word:
        								X[i + len(word) - 1] = True

        						# print(str(word) + '\t' + str([1 if x == True else 0 for x in X]))

        return X[-1]

        
    def wordBreakRecursive(self, s, wordDict):
        """
        :type s: str
        :type wordDict: List[str]
        :rtype: bool
        """
        
        if len(s) == 0:
            return True
        
        for word in wordDict:
            if (len(s) >= len(word)) and (s[0:len(word)] == word):
                if self.wordBreakRecursive(s[len(word):], wordDict) == True:
                    return True
            
        return False
                
